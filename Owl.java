public class Owl extends Snake {

    public Owl (String species, String color) {
        super(species, color);
    }

    public void setSpeciesOwl(String species) {
        setSpecies(species);
    }

    public void setColorOwl(String color) {
        setColor(color);
    }

    public String getSpeciesOwl () {
        return getSpecies();
    }

    public String getColorOwl () {
        return getColor();
    }
}

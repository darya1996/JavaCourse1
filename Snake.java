public class Snake extends Frog {

    public Snake (String species, String color) {
        super(species,color);
    }

    public void setSpeciesSnake(String species) {
        setSpecies(species);
    }

    public void setColorSnake(String color) {
        setColor(color);
    }

    public String getSpeciesSnake () {
        return getSpecies();
    }

    public String getColorSnake () {
        return getColor();
    }

}
